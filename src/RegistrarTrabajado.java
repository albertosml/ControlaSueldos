    private void tablaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaMouseClicked
        Point p = MouseInfo.getPointerInfo().getLocation();
        
        int x = p.x - tabla.getLocationOnScreen().x;
        int y = p.y - tabla.getLocationOnScreen().y;
        
        if(x > 0 && y > 0) {
            int fila = tabla.rowAtPoint(new Point(x,y));
            
            if(fila >= 0) {
                Date d = selector.getDate();
                if(d != null) {
                    SimpleDateFormat dFormat = new SimpleDateFormat("dd-MM-yyyy");
                    String date = dFormat.format(selector.getDate());
                    tabla.setValueAt(date, fila, 1);
                }
            }
        }
    }//GEN-LAST:event_tablaMouseClicked

