    private void btn_rellenarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_rellenarActionPerformed
        String trabajador = (String) trabajadores.getValue();
        if(!trabajador.isEmpty()) {
            String [] id = trabajador.split(":");
            ArrayList<String> fechas = new ArrayList<>();
            int cont = 0;
            String fecha = (String) tabla.getValueAt(cont, 1);
            String[] dia = null;
            while(cont < 27 && (fecha = (String) tabla.getValueAt(cont, 1)) != null) {
                dia = fecha.split("-");
                fechas.add(dia[0]);
                cont++;
            }
            if(fechas.size() == 0) JOptionPane.showMessageDialog(rootPane,"No se ha elegido ninguna fecha");
            else {
                String directorio = ruta.getText();
                if(!directorio.isEmpty()) {
                    OperacionesPDF o = new OperacionesPDF();
                    try {
                        String msg = o.generatePDFRegistro(id[0],dia[1],dia[2],fechas,directorio);
                        switch (msg) {
                            case "BDerror":
                                JOptionPane.showMessageDialog(rootPane,"No está conectado a la base de datos");
                                break;
                            case "PDFerror":
                                JOptionPane.showMessageDialog(rootPane,"No se ha encontrado el PDF");
                                break;
                            default:
                                JOptionPane.showMessageDialog(rootPane,"Rellenado el registro diario");
                                break;
                        }
                    } catch (ClassNotFoundException | IOException | DocumentException ex) {
                        Logger.getLogger(RegistrarTrabajado.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
                else JOptionPane.showMessageDialog(rootPane,"No se ha elegido el directorio");
            }
        }
        else JOptionPane.showMessageDialog(rootPane,"No se ha elegido al trabajador");
    }//GEN-LAST:event_btn_rellenarActionPerformed

    private void btn_directorioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_directorioActionPerformed
        JFileChooser elector = new JFileChooser();
        elector.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
        int r = elector.showOpenDialog(null);
        File f = elector.getSelectedFile();
        if(r == JFileChooser.APPROVE_OPTION) { // Si le da a aceptar
            nombre_directorio = f.getAbsolutePath();
            ruta.setText(nombre_directorio);
        }
    }//GEN-LAST:event_btn_directorioActionPerformed

