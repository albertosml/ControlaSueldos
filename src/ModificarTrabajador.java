    public ModificarTrabajador(String id) throws ClassNotFoundException, IOException {
        this.id = id;
        initComponents();
        this.setLocationRelativeTo(null);
        this.setTitle("Modificar Trabajador  -  ControlaSueldos");
        
        OperacionesBD o = new OperacionesBD();
        ArrayList<Object> a = o.obtainTrabajador(id);
        if(a.get(0) != "error") {
            nombre.setText((String) a.get(0));
            nif.setText((String) a.get(1));
            nafiliacion.setText((String) a.get(2));
            if(a.get(3) != null) {
                byte [] b = (byte[]) a.get(3);
                InputStream in = new ByteArrayInputStream(b);
                BufferedImage image = ImageIO.read(in);
                ImageIcon fot = new ImageIcon(image);
                Icon icono = new ImageIcon(fot.getImage().getScaledInstance(imagen.getWidth(),imagen.getHeight(),Image.SCALE_DEFAULT));
                imagen.setIcon(icono);
            }
            precioHora.setText(a.get(4).toString());
            btn_activo.setSelected((boolean) a.get(5));
        }
        else {
            JOptionPane.showMessageDialog(rootPane,"No se ha encontrado a ningún trabajador con ese id.");
            this.btn_activo.setEnabled(false);
            this.btn_elegirimagen.setEnabled(false);
            this.btn_modNIF.setEnabled(false);
            this.btn_modimagen.setEnabled(false);
            this.btn_modnafiliacion.setEnabled(false);
            this.btn_modnombre.setEnabled(false);
            this.btn_modpreciohora.setEnabled(false);
        }
    }

