    private void btn_elegirimagenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_elegirimagenActionPerformed
        JFileChooser elector = new JFileChooser();
        elector.setFileFilter(new FileNameExtensionFilter("Imágenes","jpeg","jpg","gif","bmp","png"));
        int r = elector.showOpenDialog(null);
        File f = elector.getSelectedFile();
        if(r == JFileChooser.APPROVE_OPTION) { // Si le da a aceptar
            nombre_archivo = f.getAbsolutePath();
            firma.setText(nombre_archivo);
        }
    }//GEN-LAST:event_btn_elegirimagenActionPerformed

    private void btn_modnombreActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_modnombreActionPerformed
        String name = nombre.getText();
        if(!name.isEmpty() && name.length() <= 60 ) {
            OperacionesBD o = new OperacionesBD();
            try {
                o.updateTrabajador(id, name, 1);
            } catch (ClassNotFoundException | IOException ex) {
                Logger.getLogger(ModificarTrabajador.class.getName()).log(Level.SEVERE, null, ex);
            }
            JOptionPane.showMessageDialog(rootPane,"El cambio de nombre del trabajador se ha realizado con éxito");
        }
        else JOptionPane.showMessageDialog(rootPane,"No se ha introducido el nuevo nombre del trabajador o este es demasiado largo");
    }//GEN-LAST:event_btn_modnombreActionPerformed

    private void btn_modNIFActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_modNIFActionPerformed
        String NIF = nif.getText();
        if(NIF.matches("[0-9A-Z]+") && NIF.length() == 9) {
            OperacionesBD o = new OperacionesBD();
            try {
                o.updateTrabajador(id, NIF, 2);
            } catch (ClassNotFoundException | IOException ex) {
                Logger.getLogger(ModificarTrabajador.class.getName()).log(Level.SEVERE, null, ex);
            }
            JOptionPane.showMessageDialog(rootPane,"El cambio de NIF del trabajador se ha realizado con éxito");
        }
        else JOptionPane.showMessageDialog(rootPane,"No se ha introducido el NIF del trabajador, debe tener 9 caracteres");
    }//GEN-LAST:event_btn_modNIFActionPerformed

