    private void btn_modimagenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_modimagenActionPerformed
        String ruta_firma = firma.getText();
        if(!ruta_firma.isEmpty()) {
            OperacionesBD o = new OperacionesBD();
            try {
                o.updateTrabajador(id, ruta_firma , 4);
            } catch (ClassNotFoundException | IOException ex) {
                Logger.getLogger(ModificarTrabajador.class.getName()).log(Level.SEVERE, null, ex);
            }
            JOptionPane.showMessageDialog(rootPane,"El cambio de la firma del trabajador se ha realizado con éxito");
            ImageIcon fot = new ImageIcon(ruta_firma);
            Icon icono = new ImageIcon(fot.getImage().getScaledInstance(imagen.getWidth(),imagen.getHeight(),Image.SCALE_DEFAULT));
            imagen.setIcon(icono);
            firma.setText("");
        }
        else JOptionPane.showMessageDialog(rootPane,"No se ha introducido la imagen de la firma del trabajador");
    }//GEN-LAST:event_btn_modimagenActionPerformed

